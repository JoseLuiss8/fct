# suma y resta  (+ y -)
suma = 12 + 5
resta = 12 - 5

# multiplicacion y division (* y /)
multiplicacion = 12 * 5
division = 12 / 5 # devuelve un dato float (decimal)

# exponenciales (potencias) (**)
exponente = 12**5

# division baja (devuelve la parte entera, redondeando a la baja el numero obtenido) (//)
division_baja = 12 // 5

# resto o modulo (devuelve el resto) (%)
resto = 12 % 5

print(suma)

print(resta)

print(multiplicacion)

print(division)

print(exponente)

print(division_baja)

print(resto)

